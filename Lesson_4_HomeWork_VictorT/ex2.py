"""
Citiţi de la tastatură 2 valori.
Verificaţi dacă ele sunt egale sau nu şi afişaţi la ecran.
"""
first_value = input("Introduceti prima valoare: ")
second_value = input("Introduceti a doua valoare: ")
if first_value == second_value:
    print("Valorile sunt echivalente.")
else:
    print("Valorile difera.")